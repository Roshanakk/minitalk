#include "libft.h"

void	ft_putstr_fd(char *s)
{
	int	i;
	int fd;

	i = 0;
	fd = 1;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
}